//串口接收回调函数
extern int8_t carmode;
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if( huart == &huart3)//判断中断源
	{
		if(Usart3ReceiveData == 'A') motorPidSetSpeed(1,1);//前运动
		if(Usart3ReceiveData == 'B') motorPidSetSpeed(-1,-1);//后运动
		if(Usart3ReceiveData == 'C') motorPidSetSpeed(0,0);//停止
		if(Usart3ReceiveData == 'D') motorPidSetSpeed(1,2);//左边运动	
		if(Usart3ReceiveData == 'E') motorPidSetSpeed(2,1);//右边运动
		if(Usart3ReceiveData == 'F') motorSpeedUp();//加速
		if(Usart3ReceiveData == 'G') motorSpeedCut();//减速
		
		if(Usart3ReceiveData == 'H')//转向90度
		{		
			//只有当前目标值 <= 180，才允许再加90°
			if(pidMPU6050YawMovement.target_val <= 180) pidMPU6050YawMovement.target_val += 90;//目标值
		}
		if(Usart3ReceiveData == 'I')//转回90度
		{				
			//只有当前目标值 >= -180，才允许再减90°
			if(pidMPU6050YawMovement.target_val >= -180) pidMPU6050YawMovement.target_val -= 90;//目标值
    }	
    if(Usart3ReceiveData == 'J')//
    {
      carmode++;
      if(carmode >= 6) carmode=0;
    }
    if(Usart3ReceiveData == 'K')
    {
      carmode = 0;//回到模式0
    }
		HAL_UART_Receive_IT( &huart3, &Usart3ReceiveData, 1);//继续进行中断接收
	}
		
		
}
